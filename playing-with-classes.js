class User {
    constructor(name, password){
        this.name = name;
        this.password = password;
        this.todos = [];
        this.idAcc = 0;
    }

    addTodo(todoObj){
        this.todos.push(todoObj);
    }

    saveUser(){
        const prepared = JSON.stringify(this);
        localStorage.setItem(this.name, prepared);
    }
}